#include<iostream>
using namespace std;
int main(){
    int a;
    cout<<"size of integers\n"<<sizeof(a)<<endl;
    float b;
    cout<<"size of float\n"<<sizeof(b)<<endl;
    char c;
    cout<<"size of characters\n"<<sizeof(c)<<endl;
    bool d;
    cout<<"size of boolean\n"<<sizeof(d)<<endl;
    short int i;
    cout<<"size of short\n"<<sizeof(i)<<endl;
    long int j;
    cout<<"size of long\n"<<sizeof(j)<<endl;
    return 0;
    }